  sha256 intel: "8221c100140cf4f490a04652e213884a3f8a4a24bfcaf4cec2f54d2d2af0e7d1",
         arm:   "006376b09ca8e160bd51c442f5811d152438c678fe8932fb8399c52eface7714"

