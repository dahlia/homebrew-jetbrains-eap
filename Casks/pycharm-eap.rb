  version "2023.2,232.5150.120"
  sha256 intel: "211e4889b0cd3caef3e2e305968cf1d0d6bd7376481556d519cd69047f0eadde",
         arm:   "734b5ff727ded920ac3f0be00c0eff2555ad0d34638dab8c7dbcd042d7f00bfb"

